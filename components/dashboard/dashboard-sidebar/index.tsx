export function DashboardSidebar({ className }: DashboardSidebarProps) {
  const pathname = usePathname();
  const { newOrderCount } = useVendorOrders();

  return (
    <aside
      className={cn(
        "sticky top-16 h-[calc(100vh-4rem)] w-60 shrink-0 border-r border-border bg-background xl:w-64",
        className
      )}
    >
      <nav className="flex flex-col gap-1 p-4">
        {NAV_ITEMS.filter((item) => item.icon !== "more").map((item) => {
          const isActive = pathname.startsWith(item.href);
          return (
            <Link
              key={item.href}
              href={item.href}
              className={cn(
                "relative flex items-center gap-3 rounded-lg px-3 py-2.5 text-sm font-medium transition-colors",
                isActive
                  ? "border-l-2 border-primary-500 bg-primary-50 text-primary-800"
                  : "text-foreground-muted hover:bg-background-muted hover:text-foreground"
              )}
            >
              <NavIcon icon={item.icon} className="size-5" />
              {item.label}
              {item.icon === "orders" && newOrderCount > 0 && (
                <span className="ml-auto inline-flex size-5 items-center justify-center rounded-full bg-error-500 text-[10px] font-bold text-white">
                  {newOrderCount}
                </span>
              )}
            </Link>
          );
        })}

        <hr className="my-2 border-border" />

        {SIDEBAR_EXTRA_ITEMS.map((item) => {
          const isActive = pathname.startsWith(item.href);
          return (
            <Link
              key={item.href}
              href={item.href}
              className={cn(
                "flex items-center gap-3 rounded-lg px-3 py-2.5 text-sm font-medium transition-colors",
                isActive
                  ? "border-l-2 border-primary-500 bg-primary-50 text-primary-800"
                  : "text-foreground-muted hover:bg-background-muted hover:text-foreground"
              )}
            >
              <NavIcon icon={item.icon} className="size-5" />
              {item.label}
            </Link>
          );
        })}
      </nav>
    </aside>
  );
}

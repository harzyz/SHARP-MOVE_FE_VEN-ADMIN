export function DashboardBottomNav({ className }: DashboardBottomNavProps) {
  const pathname = usePathname();
  const { newOrderCount } = useVendorOrders();

  return (
    <nav
      className={cn(
        "fixed bottom-0 left-0 right-0 z-[300] border-t border-border bg-background shadow-lg",
        className
      )}
    >
      <div className="flex h-16 items-center justify-around">
        {NAV_ITEMS.map((item) => {
          const isActive = item.icon === "more"
            ? ["/more", "/finance", "/settings", "/support"].some((p) => pathname.startsWith(p))
            : pathname.startsWith(item.href);
          return (
            <Link
              key={item.href}
              href={item.href}
              className={cn(
                "relative flex flex-col items-center gap-0.5 px-3 py-1 text-xs font-medium transition-colors",
                isActive ? "text-primary-800" : "text-foreground-muted"
              )}
            >
              <span className="relative">
                <NavIcon icon={item.icon} className="size-6" />
                {item.icon === "orders" && newOrderCount > 0 && (
                  <span className="absolute -right-1.5 -top-1 flex size-4 items-center justify-center rounded-full bg-error-500 text-[9px] font-bold text-white">
                    {newOrderCount > 9 ? "9+" : newOrderCount}
                  </span>
                )}
              </span>
              {item.label}
            </Link>
          );
        })}
      </div>
    </nav>
  );
}
